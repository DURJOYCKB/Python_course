str = "Durjoy Chakraborty"

print(str[3 : 6])                         #joy
print(str[7:len(str)])                    #Chakraborty
      
print(str[:6])    #[0:6]                  #Durjoy
print(str[7:])    #[7:len(str)]           #Chakraborty

#Backward Slicing...(Negative Index).
str = "Apple"
print(str[-5:-2])                         #App
